// Utility functions

/**
 * Add two numbers
 * @param {number} a - First number
 * @param {number} b - Second number
 * @returns {number} The sum of a and b
 */
function add(a, b) {
    return a + b;
  }
  
  /**
   * Subtract two numbers
   * @param {number} a - First number
   * @param {number} b - Second number
   * @returns {number} The difference between a and b
   */
  function subtract(a, b) {
    return a - b;
  }
  
  module.exports = {
    add,
    subtract
  };